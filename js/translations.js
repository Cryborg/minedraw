// Function to get French name from texture path
export function getTextureFrenchName(texturePath) {
    // Extract filename without extension
    const filename = texturePath.split('/').pop().replace('.png', '');

    // Check if we have a direct translation
    if (textureTranslations[filename]) {
        return textureTranslations[filename];
    }

    // Otherwise, beautify the filename
    return beautifyTextureName(filename);
}

// Beautify texture name when no translation exists
function beautifyTextureName(filename) {
    return filename
        .replace(/_/g, ' ')  // Replace underscores with spaces
        .replace(/\b\w/g, char => char.toUpperCase());  // Capitalize first letter of each word
}
